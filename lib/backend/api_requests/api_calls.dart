class CostEstimationCall {
  static Future<ApiCallResponse> call({
    String? imageUrl = '',
  }) async {
    final ffApiRequestBody = '''
{
  "img_url": "${imageUrl}"
}''';
    return ApiManager.instance.makeApiCall(
      callName: 'CostEstimation',
      apiUrl:
          'https://organic-product-fabrication-cost.onrender.com/estimate-cost',
      callType: ApiCallType.POST,
      headers: {
        'Content-Type': 'application/json',
      },
      params: {},
      body: ffApiRequestBody,
      bodyType: BodyType.JSON,
      returnBody: true,
      encodeBodyUtf8: false,
      decodeUtf8: false,
      cache: false,
      isStreamingApi: false,
      alwaysAllowBody: false,
    );
  }

  static double? lkr(dynamic response) => castToType<double>(getJsonField(
        response,
        r'''$.lkr_cost''',
      ));
  static double? usd(dynamic response) => castToType<double>(getJsonField(
        response,
        r'''$.usd_cost''',
      ));
  static String? pType(dynamic response) => castToType<String>(getJsonField(
        response,
        r'''$.product_type''',
      ));
}

